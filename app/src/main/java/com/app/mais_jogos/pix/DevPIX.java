package com.app.mais_jogos.pix;

public class DevPIX {

    private String pix;
    private String tipoPix;
    private double valorPag;
    private Integer idDev;


    public String getPix() {
        return pix;
    }

    public String getTipoPix() {
        return tipoPix;
    }

    public double getValorPag() {
        return valorPag;
    }

    public Integer getIdDev() {
        return idDev;
    }

    public void setPix(String pix) {
        this.pix = pix;
    }

    public void setTipoPix(String tipoPix) {
        this.tipoPix = tipoPix;
    }

    public void setValorPag(double valorPag) {
        this.valorPag = valorPag;
    }

    public void setIdDev(Integer idDev) {
        this.idDev = idDev;
    }
}
